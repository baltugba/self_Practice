package day04_practice;

public class Circle {
    public static void main(String[] args) {

        int radius = 5;
        double pi = 3.14;
        double area = pi*(radius*radius);
        double perimeters = 2*(pi*radius);

        System.out.println("Area of the circle is " + area +"\n" +"Perimeter of the circle is " + perimeters);





    }
}
